rm(list=ls())

#install.packages("mlbench")
library(mlbench)
data(Sonar)

## data 파악 및 전처리
str(Sonar)
# 61개 변수로 이루어진 208개의 데이터
# v1~v60의 설명변수는 연속형변수
# 종속변수 Y인 Class는 범주형변수

?Sonar
# Label은 돌이면 "R", 금속이면 "M"
unique(Sonar$Class)
# M, R 총 2개의 범주로 이루어진 변수임을 확인

#Sonar의 종속변수 Class를 0,1 값으로 바꿔줌
Sonar$Class <- as.character(Sonar$Class)
Sonar$Class[Sonar$Class=="M"] <- 0
Sonar$Class[Sonar$Class=="R"] <- 1
Sonar$Class <- as.factor(Sonar$Class)

str(Sonar)

## train, test 7:3으로 나누기
set.seed(100)
index <- sample(1:nrow(Sonar), nrow(Sonar)*0.7, replace = F)
sonar_test <- Sonar[-index,-61]
sonar_train <- Sonar[index,-61]
sonar_test_label <- Sonar[-index,61]
sonar_train_label <- Sonar[index,61]

sonar.pca<-prcomp(sonar_train,center=T,scale=T)
?prcomp
# SVD(Singular Value Decomposition) 이용
# prcomp(x, retx = TRUE, center = TRUE, scale. = FALSE, tol = NULL, rank. = NULL, ...)
# x : data, retx : 변수축 회전 여부, center=zero:원점 설정 여부, scale : 표준화여부
# prcomp : sdev, rotation(eigenvetors), center, scale, x(principal component)

## 변수의 개수 결정(elbow point & Cumulative Proportion)
plot(sonar.pca,type="l")
summary(sonar.pca)
# Elbow point인 PC4까지로는 53%정도까지 설명할 수 있다
min(which(summary(sonar.pca)[[6]][3,] >= 0.8))
# Cumulative Proportion(누적비율)을 통해 PC13까지 80%를 설명할 수 있다
# summary(sonar.pca)[[6]]  =>  Importance of components 부분
# summary(sonar.pca)[[6]][3,]  =>  Cumulative Proportion 부분
# 누적설명력이 0.8이상인것들의 인덱스 뽑아준다

# 기존 data matrix와 Eigenvetor 내적
# 행렬곱을 통해 새로운 주성분을 만드는 과정
# 새로운 주성분 PC의 성분의 구성 값 계산
# rotation이 계수값
# 주성분 구성할때의 pc1 <- a*x1 + b*x2 +... 에서 a,b
trainPRC<-as.matrix(sonar_train) %*% sonar.pca$rotation
testPRC<-as.matrix(sonar_test) %*% sonar.pca$rotation

train<-cbind(as.data.frame(trainPRC),sonar_train_label)
test<-cbind(as.data.frame(testPRC),sonar_test_label)

colnames(train)[61] <- "label"; colnames(test)[61] <- "label"

str(train)
## 만든 주성분으로 회귀분석
fit <- glm(label~., family = "binomial", data=train)
summary(fit)

## 예측
fit_pred <- predict(fit,type="response",newdata = test)
test_pred<- round(fit_pred)

table<-table(sonar_test_label,test_pred)
table
sum(diag(table))/sum(table)

